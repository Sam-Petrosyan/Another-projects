int	ft_atoi(char *str)
{
	int	i;
	int	x;
	int	y;

	y = 0;
	x = 1;
	i = 0;
	while (str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\v' || str[i] == '\f'
		|| str[i] == ' ' || str[i] == '\r')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			x = -x;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		y = y * 10 + str[i] - 48;
		i++;
	}	
	return (y * x);
}
