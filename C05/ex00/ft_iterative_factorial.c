int	ft_iterative_factorial(int nb)
{
	int	i;
	int	x;

	if (nb < 0)
		return (0);
	if (nb <= 1)
		return (1);
	i = 1;
	x = 1;
	while (i <= nb)
	{
		x = x * i;
		i++;
	}
	return (x);
}
