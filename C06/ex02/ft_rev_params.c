#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	x;

	x = argc - 1;
	while (x >= 1)
	{
		i = 0;
		while (argv[x][i])
		{
			write(1, &argv[x][i], 1);
			i++;
		}
		write(1, "\n", 1);
		x--;
	}
	return (0);
}
